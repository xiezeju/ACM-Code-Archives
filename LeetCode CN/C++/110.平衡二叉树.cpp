// @lc code=start
/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left),
 * right(right) {}
 * };
 */
class Solution {
    public:
        pair<bool, int> dfs(TreeNode* p) {
            if (p == nullptr) {
                return {true, 0};
            }

            if (!p->left && !p->right) {
                return {true, 1};
            }

            auto [balance_l, height_l] = dfs(p->left);
            auto [balance_r, height_r] = dfs(p->right);

            bool balance =
                (abs(height_l - height_r) <= 1) && balance_l && balance_r;
            int height = max(height_l, height_r) + 1;

            return {balance, height};
        }

        bool isBalanced(TreeNode* root) { return dfs(root).first; }
};

