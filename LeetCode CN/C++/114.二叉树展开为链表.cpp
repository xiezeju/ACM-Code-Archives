/**
 * Definition for a binary tree node.
 * struct TreeNode {
 *     int val;
 *     TreeNode *left;
 *     TreeNode *right;
 *     TreeNode() : val(0), left(nullptr), right(nullptr) {}
 *     TreeNode(int x) : val(x), left(nullptr), right(nullptr) {}
 *     TreeNode(int x, TreeNode *left, TreeNode *right) : val(x), left(left), right(right) {}
 * };
 */
class Solution {
public:

    void flatten(TreeNode* root) {
        TreeNode* p = root;
        while(p!=nullptr){
            if(p->left!=nullptr){
                TreeNode *next = p->left; //记录当前节点的左节点
                TreeNode *process = p->left; //process记录左子树的最右下方的节点
                while(process->right!=nullptr) //寻找左子树最右下方的点
                    process = process->right; 
                process->right = p->right; //将右子树拼接在左子树最右下方的点右边
                p->left=nullptr;
                p->right=next; //把左子树挪到右边
            }
            p = p->right; //在修改好子树后向右下方走，相当于访问原来子树的左节点
        }
    }

};
// @lc code=end

